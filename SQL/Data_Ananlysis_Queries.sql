/*Q15] What percentage of warranty claims occur within:
0–30 days
31–90 days
91–180 days
180+ days*/

with claim_table
as (
select s.sale_date,
	   s.sale_id,
	   w.claim_id,
	   w.claim_date,
	   CASE
	   		when w.claim_date <s.sale_date then 'data error claim before sale'
	   		
			when w.claim_date >=s.sale_date and  w.claim_date <= s.sale_date + interval '30 days' 
				then 'BEFORE FIRST MONTH'
			
			when w.claim_date > s.sale_date + interval '30 days' and w.claim_date<= s.sale_date + interval '90 days' 
				then 'BETWEEN FIRST AND THIRD MONTH'       
		    
			when w.claim_date > s.sale_date + interval '90 days' and w.claim_date <= s.sale_date + interval '180 days' 
				then  'BETWEEN THIRD AND SIXTH MONTH'                                      
			
			when w.claim_date > s.sale_date + interval '180 days' 
				then 'AFTER SIXTH MONTHS'
			
			else 'UNKNOWN EXCEPTION'
			end as CLAIM_OCCURANCE
from warranty w
join sales s
on w.sale_id = s.sale_id
)
select claim_occurance,
	   count(claim_occurance) as claims,
	   ((count(claim_occurance) :: "numeric" )/((select count(claim_id) from claim_table) :: "numeric")) * 100 as claim_percent
from claim_table
group by claim_occurance
order by claim_percent desc

                                       

-- Q16] What is the average time between purchase and warranty claim for each product category? ✅ (My question)

select c.category_id,
	   c.category_name,
	   avg(claim_date - sale_date) as avg_time,
	   justify_interval(avg(claim_date - sale_date) * interval'1 day') as avg_time_months
from sales s  
join  warranty w
on s.sale_id = w.sale_id
join products p
on p.product_id = s.product_id
join category c
on p.category_id = c.category_id
where claim_date >= sale_date
group by c.category_id,c.category_name


--Q17] Which products generate the highest revenue but also have the highest warranty claim rates? 

with claim_revenue
as
(select product_name,
		p.product_id,
	    sum(price * quantity) as revenue,
	    sum (s.quantity) as total_units, 
	    count(claim_id) as claims
from sales s  
left join  warranty w
on s.sale_id = w.sale_id
join products p
on p.product_id = s.product_id
group by p.product_name,p.product_id
order by total_units desc),

kpi_metrics AS
(
select *,
		(claims ::"numeric")/(total_units::"numeric")*100 as claim_percent
FROM claim_revenue
)

select product_name,
	   product_id,
	   revenue,
	   dense_rank() over(order by revenue desc) as revenue_rank,
	   total_units,
	   claims,
	   claim_percent,
	   dense_rank() over(order by claim_percent desc) as claim_percent_rank
from kpi_metrics
order by revenue desc ,claim_percent desc





