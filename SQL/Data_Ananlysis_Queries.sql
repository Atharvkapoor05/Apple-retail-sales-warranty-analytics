-- Q8] Which stores consistently rank in the Top 5 by sales across multiple years?


WITH revenue_table AS (
    SELECT
        st.store_name,
        s.store_id,
        SUM(s.quantity * p.price) AS total_revenue,
        EXTRACT(YEAR FROM s.sale_date) AS sale_year
FROM sales s 
JOIN products p 
ON s.product_id = p.product_id 
JOIN stores st 
ON s.store_id = st.store_id
GROUP BY st.store_name, s.store_id, EXTRACT(YEAR FROM s.sale_date)
),

ranked_store AS (
SELECT	
	 store_name,
	 store_id,
     total_revenue,
     sale_year,
     DENSE_RANK() OVER(PARTITION BY sale_year ORDER BY total_revenue DESC) AS str_rnk
FROM revenue_table
)
SELECT 
    store_id,
    store_name,
    COUNT(DISTINCT sale_year) AS total_years_tracked,
    SUM(CASE WHEN str_rnk <= 5 THEN 1 ELSE 0 END) AS times_in_top_5,
    MIN(str_rnk) AS best_rank_ever,
    MAX(str_rnk) AS worst_rank_ever
FROM ranked_store
GROUP BY store_id, store_name 
ORDER BY times_in_top_5 DESC, total_years_tracked DESC;



