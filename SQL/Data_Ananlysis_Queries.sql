-- Q6] Analyze the year-over-year growth rate for each store.

with revenue_table 
as
(SELECT 
	s.store_id,
	sum(((s.quantity * p.price))) as current_year_total_revenue,
	extract( year from sale_date) as sale_year
from sales s 
join products p 
on   s.product_id = p.product_id 
group by s.store_id,sale_year) 

SELECT rt.store_id,s2.store_name,
	   sale_year,
	   current_year_total_revenue,
	   lag(current_year_total_revenue) over( partition by rt.store_id order by sale_year) as prev_year_total_revenue,
  	   round(((((current_year_total_revenue- lag(current_year_total_revenue) over( partition by 
		 rt.store_id order by sale_year))/lag(current_year_total_revenue) over( partition by 
		 rt.store_id order by sale_year)))*100)) as YOY_GROWTH
from revenue_table rt 
join stores s2 
on rt.store_id=s2.store_id

