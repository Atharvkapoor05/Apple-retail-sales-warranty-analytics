--Q10] Identify the least-selling product in each country for every year

with product_detail as 
(
SELECT s.product_id,
 	   sum(quantity) as quantitys,
	   extract(year from s.sale_date) as years,
	   st.country	
from sales s
join stores st
on s.store_id = st.store_id
group by s.product_id,years,st.country
),

rank_table as
(
select *,
	   dense_rank() over(partition by country,years 
	   order by quantitys) as rnk	
from product_detail)

