	   p.product_name,
	   r.quantitys,
	   r.years,
	   r.country,
	   r.rnk
from rank_table r
join products p
on r.product_id = p.product_id
where rnk = 1 



/*Q11 Analyze product sales trends by lifecycle stage:
0–6 months
6–12 months
12–18 months
18+ months*/

select	p.product_name,
		p.product_id, 
		sum(s.quantity) as units_sold,
		CASE	
			when s.sale_date < p.launch_date + interval '6 months' 
				then 'First 6 months'
			
			when s.sale_date >= p.launch_date + interval '6 months'
				 and S.sale_Date < p.launch_date + interval '12 months' 
				 	then 'Between 6 - 12 months'
			
			when s.sale_date >= p.launch_date + interval '12 months' 
				 and S.sale_Date < p.launch_date + interval '18 months' 
				 	then 'Between 12 - 18 months'
			
			when s.sale_date >= p.launch_date +interval '18 months' 
					then ' post 18 months'	 
		else 'non_valid'	
		end as life_cycle
from sales s
join products p
on s.product_id = p.product_id
group by p.product_name,p.product_id,life_cycle
order by product_name

---Q12] Which products experience the largest decline in sales after 12 months from launch?


--- 493143 tuples EXISTS in Dataset where sale date preceds launch date I.E. sale was done brfore launch of product  
--- using belown table as CTE we can count number of rows where sale_date < launch_date 

with sales_time as
(select p.product_name,
		p.product_id,
		s.quantity,
		p.launch_date,
		s.sale_date,
		case
			when s.sale_date <p.launch_date 
				THEN 'Data error : sold before launch'
			
			when s.sale_date BETWEEN p.launch_date and p.launch_date + INTERVAL '12 months' 
				THEN 'within 1 year'
			
			when s.sale_date >  p.launch_date + INTERVAL '12 months' 
				THEN 'post 1 year' 	

			else 'unknown exception'
			END as time_range
from sales s
join products p
on s.product_id = p.product_id),

sales_record as
(select  product_name,
		product_id,
		sum( case 
				when time_range = 'within 1 year' 
					THEN quantity else 0 end) as first_year,
		sum( CASE 
				WHEN time_range = 'post 1 year'	 
					THEN quantity else 0 end) as post_1_year 		

from sales_time 
group by product_name,product_id)

SELECT 	*, 
		(first_year - post_1_year) as sales_decline
from sales_record
order by sales_decline desc
          

